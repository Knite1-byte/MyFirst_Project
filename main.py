import random

# Определяем списки слов по категориям
существительные = ['кошка', 'собака', 'слон', 'человек', 'робот', 'дракон']
глаголы = ['бежит', 'спит', 'прыгает', 'летает', 'строит', 'мечтает']
прилагательные = ['быстрый', 'медленный', 'огромный', 'маленький', 'зеленый', 'красный']
наречия = ['вчера', 'сегодня', 'внезапно', 'тихо', 'громко', 'везде']
места = ['в лесу', 'на Марсе', 'в городе', 'под водой', 'на луне']

# Выбираем случайные слова из каждого списка
случайное_существительное = random.choice(существительные)
случайный_глагол = random.choice(глаголы)
случайное_прилагательное = random.choice(прилагательные)
случайное_наречие = random.choice(наречия)
случайное_место = random.choice(места)

# Собираем предложение
история = f"{случайное_наречие}, {случайное_прилагательное} {случайное_существительное} {случайный_глагол} {случайное_место}."

# Выводим результат
print(история)